def month_name_to_number(x):
    if x == 'January':
        return 1
    elif x == 'February':
        return 2
    elif x == 'March':
        return 3
    elif x == 'April':
        return 4
    elif x == 'May':
        return 5
    elif x == 'June':
        return 6
    elif x == 'July':
        return 7
    elif x == 'August':
        return 8
    elif x == 'September':
        return 9
    elif x == 'October':
        return 10
    elif x == 'November':
        return 11
    elif x == 'December':
        return 12
    else:
        return 'error'